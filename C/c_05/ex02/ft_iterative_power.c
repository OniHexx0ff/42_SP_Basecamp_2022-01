int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	result;

	i = 2;
	result = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	while (i++ <= power)
		result *= nb;
	return (result);
}
