void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	helper;

	i = size - 1;
	while (i > 0)
	{
		j = 0;
		while (j < i)
		{
			if (tab[j] > tab[j + 1])
			{
				helper = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = helper;
			}
				j++;
		}
		i--;
	}
}
